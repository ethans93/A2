/**
 * The purpose of this program is to show multiple expressions and their respective outcomes
 * @author Ethan Steen
 * @version 1.0
 */
public class Assignment2Part1 {
public static void main(String [] args){
	
	System.out.println("1 / 2 = " + 1/2);
	//output is int
	System.out.println("1 % 2 = " + 1%2);
	//output is int
	System.out.println("1.0/ 2 = " + 1.0/2);
	//output is double
	System.out.println("5 + 7 / 2 = " + (5+7/2));
	//output is int
	System.out.println("'Beat + ' ' + Army' = " + "Beat" + " " + "Army");
	//output is String
	System.out.println("6 + 13/5 - 35%3 = " + (6+13/5-35%3));
	//output is int
	System.out.println("3.5 * (5/4) = " + (3.5*(5/4)));
	//output is double
	System.out.println("(3.5 * 5) / 4 = " + ((3.5*5)/4));
	//output is double
}
}
